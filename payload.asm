	[BITS 16]
	[ORG 0x8800]

%include "bios.h"

	MOV SI, PAYLOAD_STRING
	CALL PRINT
	CLI
	HLT

PRINT:
	PUSHA
	MOV AH, PUTCHAR
PRINTLOOP:
	LODSB
	OR AL, AL
	JZ DONEPRINT
	INT TEXT_SERVICE
	JMP PRINTLOOP
DONEPRINT:
	MOV AL, 13
	POPA
	RET

times (1024 * 2) db 69

PAYLOAD_STRING: DB "Kernel successfully entered.", 13, 10, 0
